def draw_omikuji(seed_by_today: bool = True) -> dict:
    """今日の運勢おみくじを返す。"""

    fortune = random.choices(FORTUNES, weights=FORTUNE_WEIGHTS, k=1)[0]
    lucky_item = random.choice(LUCKY_ITEMS)
    message = random.choice(MESSAGES)

